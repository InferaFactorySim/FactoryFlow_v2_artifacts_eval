import importlib.util

def import_dynamic_module(path):
    """
    Dynamically import a Python file as a module at runtime.

    Assigns a unique module name derived from the file path hash so that
    multiple different files can be loaded without name collisions.

    Args:
        path: Absolute or relative path to the ``.py`` file to import.

    Returns:
        The imported module object, with all its top-level names accessible
        as attributes.
    """
    name = f"dyn_{abs(hash(path))}"
    spec = importlib.util.spec_from_file_location(name, path)
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod







