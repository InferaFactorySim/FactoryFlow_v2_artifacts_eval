def build_factorysimpy_api_docs(folder_path: str, params: Dict) -> Dict:
    """
    Scan a folder for Python reference files and bundle them with schema params.

    Reads every ``*.py`` file in *folder_path* and returns a single dict that
    the AMG pipeline uses as its ``factorysimpy_api_docs`` state value.

    Args:
        folder_path: Path to the directory containing FactorySimPy example/reference files.
        params: Schema params dict describing supported node/edge types and their
            constructor arguments (see ``PARAMS``).

    Returns:
        A dict with three keys:
            - ``"filenames"``: list of discovered ``.py`` file names.
            - ``"reference_code"``: mapping of filename → file contents.
            - ``"schema_params"``: the *params* argument passed in.

    Raises:
        FileNotFoundError: If *folder_path* does not exist or is not a directory.
    """
    code_contents = {}
    folder = Path(folder_path)
    
    if not folder.exists() or not folder.is_dir():
        logger.error(f"Folder not found: {folder_path}")
        raise FileNotFoundError(f"Directory {folder_path} does not exist.")

    # Automatically find all Python files in the folder
    py_files = list(folder.glob("*.py"))
    for fpath in py_files:
        with open(fpath, "r", encoding="utf-8") as f:
            code_contents[fpath.name] = f.read()
    
    logger.debug(f"Loaded {len(code_contents)} reference files from {folder_path}")
    
    return {
        "filenames": [f.name for f in py_files],
        "reference_code": code_contents,
        "schema_params": params
    }


