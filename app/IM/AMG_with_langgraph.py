def load_code_files(filenames: List[str], folder: str = ".") -> Dict[str, str]:
    """
    Load the contents of a specific list of Python files from a folder.

    Args:
        filenames: List of file names (not full paths) to load.
        folder: Directory in which to look for the files. Defaults to the
            current working directory.

    Returns:
        A dict mapping each filename to its text contents.

    Raises:
        FileNotFoundError: If any file in *filenames* is not found in *folder*.
    """
    out = {}
    for fname in filenames:
        path = os.path.join(folder, fname)
        if not os.path.exists(path):
            raise FileNotFoundError(f"Missing file: {path}")
        with open(path, "r", encoding="utf-8") as f:
            out[fname] = f.read()
    return out


