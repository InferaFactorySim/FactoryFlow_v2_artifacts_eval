def build_amg_graph():
    """
    Construct and compile the two-node AMG LangGraph pipeline.

    Graph topology:
        reasoner  →  amg

    - ``reasoner``: infers assumptions from the user description.
    - ``amg``: generates Python code using the description + assumptions.

    Returns:
        A compiled LangGraph ``CompiledGraph`` ready to be invoked with an
        ``AMGState`` dict.
    """
    graph = StateGraph(AMGState)
    
    #added only not connected
    
    #reasoner-->amg--->edit
    graph.add_node("reasoner", reasoner_node)
    graph.add_node("amg", amg_llm_node)
    

    graph.add_edge("reasoner", "amg")
    

    #graph.add_edge("edit", "edit")

    graph.set_entry_point("reasoner")
    graph.set_finish_point("amg")    # IMPORTANT

    return graph.compile()


