def reasoner_node(state: AMGState) -> Dict:
    """
    LangGraph node — infer missing assumptions from a system description.

    Builds a prompt from the description and FactorySimPy API docs, invokes the
    LLM, and extracts a numbered list of assumptions that fill in details not
    specified by the user (e.g., default edge types, buffer counts, machine IDs).

    No code is generated here; only assumptions are produced.

    Args:
        state: Current ``AMGState`` containing at least ``description`` and
            ``factorysimpy_api_docs``.

    Returns:
        A partial state dict ``{"assumptions": <assumption_text>}`` to be merged
        into the graph state.
    """
    #prompt_begins = time.perf_counter()
    llm_starts = time.perf_counter()

    description = state["description"]
    api_docs = state["factorysimpy_api_docs"]
    params = api_docs["schema_params"]
    input_files = api_docs["filenames"]
    code_files = api_docs["reference_code"]
    logger.debug("Invoking Reasoner Node.")
    #logger.debug(f"Reasoner node params are {params}")

    
   


    prompt = f"""
You are the REASONER for a manufacturing model generator.

Your job is ONLY to infer assumptions needed other than what is described in the description to build the model.
DO NOT generate any code.

The description given by the user is:

### Description:
{description}

### Supported Node Types:
{params["nodes_supported_type"]}

### Supported Edge Types:
{params["edges_supported_type"]}





### Available Base Files:
{input_files}

### EXAMPLES OF CODE
Base examples:
"""

    # attach example code
    for fname, content in code_files.items():
        prompt += f"\n# FILE: {fname}\n```python\n{content}\n```"

    prompt += f"""


You must list ALL assumptions that MUST be made because
the description does NOT specify them.

Typical assumptions you must output:
- Missing node names → infer names. Add it in assumption only if not specified in description. 
- Missing machine ids → infer machine IDs. Add it in assumption only if not specified in description.
- Missing edge types → use default: Donot pass that parameter as argument.  Add in assumption- "Default value used" only if not specified in description.
- Missing node types → use default: Donot pass that parameter as argument. Add in assumption- "Default value used"  only if not specified in description.
- Missing capacities → use defaults in params.  Add it in assumption only if not specified in description.
- Missing delays, inter-arrival times → use defaults. Donot pass that parameter as argument.  Add in assumption- "Default value used" only if not specified in description.
For every component you need not say used default.
you may say a generic statement like "Used default values for all missing parameters for all components"
- Missing directions of flow → infer logical flow
- Missing split ratios → out_edge_selection = "ROUND_ROBIN" for equal splitting.
- Missing number of buffers between nodes → assume 1. This is very important to mention if not specified in description.
can be told in line. Used a single buffer where ever number of buffers not specified.
Combine all such assumptions that apply to multiple components into single line wherever possible.
Dont have to say anything about things inferred from description if already present in description

Output MUST be a clean numbered list of assumptions. Only add your assumptions if it is NOT already specified in the description. If user has provided the name of nodes/ edges/ nodetypes/edgetypes, use it and say that Node names and Types are chosen according to users description. This one line is enough
NO code. NO markdown.
Only assumptions that the llm made to fill in gaps in the description.
Do not use splitter, combiner etc. Replace them with Machine nodes.
Infer if the description has any repeated patterns of nodes and edges. Then create a class for that repeated pattern and use it in the final code.



 
ASSUMPTIONS:
<list(numberred list) all assumptions that you made that are NOT available in description ONLY>
for example if description says "a machine M1 connected to a sink S1" and does not say what type of edge is used, you must say "1. Used default edge type Buffer for edge connecting M1 and S1 as edge type not specified in description". You need not say infered M1 and S1 from description.
"""

    # prompt_ends = time.perf_counter()
    # log_time("Prompt prep inside reasoner node", prompt_ends - prompt_begins, "amg_with_langgraph")
    # llm_starts = time.perf_counter()

    resp = llm.invoke([SystemMessage(content="You make assumptions strictly according to instructions."),
                       HumanMessage(content=prompt)])
    llm_ends = time.perf_counter()
    log_time("LLM call (Reasoner Node) returned in", llm_ends - llm_starts, "amg_with_langgraph")
    record_time("reasoner", llm_ends - llm_starts, "amg_with_langgraph")
    text = resp.content

    # lowercase check
    lower = text.lower()

    if "assumptions:" in lower:
        idx = lower.find("assumptions:")
        output = text[idx + len("assumptions:"):].strip()
    else:
        output = text.strip()
    logger.debug(f"Reasoner assumptions output: {output}")

    return {"assumptions": output}


