def run_initial_from_description(system_prompt, description, folder, params):
    """
    Run the full AMG pipeline from scratch given a plain-text description.

    Builds the ``factorysimpy_api_docs`` from *folder*, initialises a fresh
    ``AMGState``, and invokes the compiled LangGraph (reasoner → amg).

    Args:
        system_prompt: High-level LLM instruction string (see ``SYSTEM_PROMPT``).
        description: Natural-language description of the manufacturing system.
        folder: Path to the directory containing FactorySimPy reference ``.py`` files.
        params: Schema params dict for supported node/edge types (see ``PARAMS``).

    Returns:
        The final ``AMGState`` dict after the graph has finished, containing
        ``"assumptions"`` and ``"code"`` populated by the LLM nodes.
    """
    #code_files = load_code_files(input_files, folder)

    prompt_prep_start = time.perf_counter()
    api_docs = build_factorysimpy_api_docs(folder, params)
    graph = build_amg_graph()

    state: AMGState = {
        "system_prompt": system_prompt,
        "description": description,
        "assumptions": "",
        "factorysimpy_api_docs": api_docs,
        
    }
    prompt_prep_ends = time.perf_counter()

    #log_time("Prompt preparation", prompt_prep_ends - prompt_prep_start, "run_initial_from_description")

    return graph.invoke(state)


