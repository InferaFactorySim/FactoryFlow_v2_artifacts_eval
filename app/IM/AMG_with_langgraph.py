def amg_llm_node(state: AMGState) -> Dict:
    """
    LangGraph node — generate FactorySimPy Python code from description + assumptions.

    Combines the system prompt, user description, inferred assumptions, and
    reference API docs into a single prompt, then invokes the LLM to produce a
    Python class that models the described manufacturing system.

    If the LLM returns an empty or very short response (known Gemini 3 Pro bug),
    one automatic retry is performed with an explicit instruction to output code.

    Markdown fences (```python ... ```) are stripped from the response before
    returning the clean source code.

    Args:
        state: Current ``AMGState`` containing ``system_prompt``, ``description``,
            ``assumptions``, and ``factorysimpy_api_docs``.

    Returns:
        A partial state dict ``{"code": <generated_python_code>}`` to be merged
        into the graph state.
    """
    # prompt_begins = time.perf_counter()
    llm_starts = time.perf_counter()
    system_prompt = state["system_prompt"]
    description = state["description"]
    
    #print(f"metadata from {datafitr_metadata}")
    assumptions = state["assumptions"]
    api_docs = state["factorysimpy_api_docs"]
    params = api_docs["schema_params"]
    input_files = api_docs["filenames"]
    code_files = api_docs["reference_code"]
    
    logger.debug("Invoking AMG LLM Node.")

    prompt = f"""
{system_prompt}+ You are the code generator node for manufacturing systems. You use description and the assumptions to generate the code. You will not modify assumptions or descriptions.

You only generate code based on description and assumptions

### DESCRIPTION
{description}

### ASSUMPTIONS
{assumptions}

### PARAMS
{params}



### INPUT PYTHON FILES
{input_files}

### EXAMPLES OF CODE
EXAMPLE CODE:
"""
   
    for fname, content in code_files.items():
        prompt += f"\n# FILE: {fname}\n```python\n{content}\n```"
    prompt+=f"the word TOP should be used to instantiate the model finally."
    
    messages = [
        SystemMessage(content=system_prompt),
        HumanMessage(content=prompt)
    ]
    
    # prompt_ends = time.perf_counter()
    # log_time("Prompt prep inside AMG LLM node", prompt_ends - prompt_begins, "amg_with_langgraph")
    
    # llm_starts = time.perf_counter()
    resp = llm.invoke(messages)
    llm_ends = time.perf_counter()
    log_time("LLM call (AMG LLM Node) returned in", llm_ends - llm_starts, "amg_with_langgraph")
    record_time("amg_gen", llm_ends - llm_starts, "amg_with_langgraph")
    

    raw_content = resp.content.strip()
    # 2. EMERGENCY RETRY for Gemini 3 Pro "Empty Output" Bug

    if not raw_content or len(raw_content) < 50:
        llm_starts_retry = time.perf_counter()
        logger.warning("Gemini 3 Pro returned empty/short output. Retrying with 'Thinking' disabled...")
        # We call it again with a forced instruction
        retry_message = messages + [HumanMessage(content="You failed to provide code. PROVIDE THE FULL CODE NOW in ```python blocks.")]
        resp = llm.invoke(retry_message)
        raw_content = resp.content.strip()
        llm_ends_retry = time.perf_counter()
        log_time("LLM retry call (AMG LLM Node) returned in", llm_ends_retry - llm_starts_retry, "amg_with_langgraph")
        record_time("amg_gen", llm_ends_retry - llm_starts_retry, "amg_with_langgraph")
    # with open("debug_amg_output.txt", "w", encoding="utf-8") as f:
    #     f.write(resp.content)
    logger.debug(f"AMG LLM output: {raw_content}")
    code = raw_content
    if code.startswith("```python"):
        code = code.replace("```python", "", 1)
    if code.endswith("```"):
        code = code.rsplit("```", 1)[0]

    clean_code = code.strip()
    
    return {"code": clean_code}






